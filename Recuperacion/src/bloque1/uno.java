package bloque1;

public class uno
{

	public static void main(String[] args)
	{
		int n = 5;
		double a = 2.5;
		char c = 'a';

		System.out.println("La variable n es: " + n);
		System.out.println("La variable a es: " + a);
		System.out.println("La variable c es: " + c);
		System.out.println("");
		
		System.out.println("La variable n+a es: " + (n+a));
		System.out.println("La variable a-n es: " + (a-n));
		System.out.println("La variable c es: " + (int)c);
		
		
	}

}
