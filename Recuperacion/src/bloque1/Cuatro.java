package bloque1;

public class Cuatro
{

	public static void main(String[] args)
	{
		int a = 4;
		int b = -6;
		System.out.println("A vale: " + a);
		System.out.println("B vale: " + b);
		System.out.println(a + (a % 2 == 0 ? " Es par." : " Es impar."));
		System.out.println(b + (b >= 0 ? " Es positivo." : " Es negativos."));

	}

}
