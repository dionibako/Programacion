package bloque6;

public class VeintisieteAlum
{

	String nombre;
	double nota;

	public VeintisieteAlum(String nombre, double notaAlum)
	{
		this.nombre = nombre;
		this.nota = notaAlum;
	}

	public String getNombre()
	{
		return nombre;
	}

	public void setNombre(String nombre)
	{
		this.nombre = nombre;
	}

	public double getNota()
	{
		return nota;
	}

	public void setNota(int nota)
	{
		this.nota = nota;
	}

}
